package com.sj;

public class Project {
	
	int id;
	String title;
	String descrp;
	String domain;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDecrp() {
		return descrp;
	}
	public void setDecrp(String descrp) {
		this.descrp = descrp;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	
}
